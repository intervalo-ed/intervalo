/**
 * Generates src/lib/catalog/{slug}.generated.ts for every course found under
 * backend/content/[slug]/course.json.
 *
 * The runtime shape is always belts -> units -> topics -> skills. If a
 * course.json omits units and provides belts.topics directly, we wrap the
 * topics under a synthetic unit keyed after the belt so both course layouts
 * share the same TS types downstream.
 *
 * Wired into `predev` / `prebuild`. Run manually with:
 *   bun run scripts/sync-catalog.ts
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync, existsSync, statSync } from "node:fs"
import { dirname, resolve, join } from "node:path"
import { fileURLToPath } from "node:url"

const here = dirname(fileURLToPath(import.meta.url))
const CONTENT_ROOT = resolve(here, "../../backend/content")
const OUT_DIR = resolve(here, "../src/lib/catalog")

// Only these courses are surfaced to the frontend. Add more as they become
// available in the dashboard UI.
const ENABLED_COURSES = new Set(["analisis", "probabilidad", "algebra"])

type RawTopic = {
  key: string
  name: string
  tooltip: string
  short_description?: string
  skills: string[]
}

type RawUnit = { key: string; name: string; description?: string; topics: RawTopic[] }

type RawBelt = {
  key: string
  headline: string
  description: string
  units?: RawUnit[]
  topics?: RawTopic[]
}

type RawExerciseType = { id: string; label: string; description: string }

type RawCourse = {
  slug: string
  name: string
  description: string
  exercise_types: RawExerciseType[]
  belts: RawBelt[]
}

type NormalBelt = { key: string; headline: string; description: string; units: RawUnit[] }
type NormalCourse = Omit<RawCourse, "belts"> & { belts: NormalBelt[] }

function normalizeBelt(belt: RawBelt): NormalBelt {
  if (belt.units && belt.units.length > 0) {
    return { key: belt.key, headline: belt.headline, description: belt.description, units: belt.units }
  }
  const topics = belt.topics ?? []
  const syntheticUnit: RawUnit = { key: belt.key, name: belt.headline, description: belt.description, topics }
  return {
    key: belt.key,
    headline: belt.headline,
    description: belt.description,
    units: [syntheticUnit],
  }
}

function generateOne({ slug }: { slug: string }) {
  const source = join(CONTENT_ROOT, slug, "course.json")
  const outFile = join(OUT_DIR, `${slug}.generated.ts`)
  const raw: RawCourse = JSON.parse(readFileSync(source, "utf8"))
  const normalized: NormalCourse = {
    ...raw,
    belts: raw.belts.map(normalizeBelt),
  }

  const beltKeys = normalized.belts.map((b) => b.key)

  const header = `// AUTO-GENERATED by scripts/sync-catalog.ts.
// Source: backend/content/${slug}/course.json
// Do not edit by hand; rerun \`bun run scripts/sync-catalog.ts\`.

`

  const types = `export type BeltKey = ${beltKeys.map((k) => JSON.stringify(k)).join(" | ")}

export interface Topic {
  key: string
  name: string
  tooltip: string
  short_description?: string
  skills: string[]
}

export interface Unit {
  key: string
  name: string
  description?: string
  topics: Topic[]
}

export interface Belt {
  key: BeltKey
  headline: string
  description: string
  units: Unit[]
}

export interface ExerciseType {
  id: string
  label: string
  description: string
}

export interface Catalog {
  slug: string
  name: string
  description: string
  belts: Belt[]
  exercise_types: ExerciseType[]
}

`

  const data = `export const catalog: Catalog = ${JSON.stringify(normalized, null, 2)}\n`

  mkdirSync(dirname(outFile), { recursive: true })
  writeFileSync(outFile, header + types + data)
  const nUnits = normalized.belts.reduce((n, b) => n + b.units.length, 0)
  console.log(`✓ Wrote ${outFile} (${normalized.belts.length} belts, ${nUnits} units)`)
}

function listCourses(): string[] {
  const found: string[] = []
  for (const entry of readdirSync(CONTENT_ROOT)) {
    const abs = join(CONTENT_ROOT, entry)
    if (!statSync(abs).isDirectory()) continue
    if (!ENABLED_COURSES.has(entry)) continue
    if (!existsSync(join(abs, "course.json"))) continue
    found.push(entry)
  }
  return found
}

for (const slug of listCourses()) {
  generateOne({ slug })
}
